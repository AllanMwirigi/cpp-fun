#include <iostream>

using namespace std;

int main()
{
    int f,s,t;
    cout << "Please enter your three numbers:"<<endl<<endl;
    cin>>f>> s>> t;

    cout<<"\nYour numbers in forward order:\n"<<endl;
    cout<<f<<endl;
    cout<<s<<endl;
    cout<<t<<endl<<endl;

    cout<<"Your numbers in reversed order:\n"<<endl;
    cout<<t<<endl;
    cout<<s<<endl;
    cout<<f<<endl;

    return 0;
}
